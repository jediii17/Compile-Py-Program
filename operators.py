# Arithmetic operators
a = 3
b = 4
print(a + b)  # 7
print(a - b)  # -1
print(a * b)  # 12
print(a / b)  # 0.75
print(a // b)  # 0
print(a % b)  # 3
print(a ** b)  # 81

# Comparison operators
x = 4
y = 8
print(x == y)  # False
print(x != y)  # True
print(x > y)  # False
print(x < y)  # True
print(x >= y)  # False
print(x <= y)  # True

# Logical operators
i = True
j = False
print(i and j)  # False
print(i or j)  # True
print(not i)   # False

# In this example, we use the arithmetic operators +, -, *, /, //, %,
# and ** to perform various mathematical operations. We also use the
# comparison operators ==, !=, >, <, >=, and <= to compare the values
# of x and y, and the logical operators and, or, and not to combine and
# negate boolean values.
